#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	x;
	int	y;

	if (*b != 0)
	{
		x = *a / *b;
		y = *a % *b;

		*a = x;
		*b = y;
	}
	else
	{
		*a = 0;
		*b = 0;
	}
}
